function getMapTile (map, x, y) {
    return map[ y * mapColumns + x ];
}

var pixelsPerTile = 20;
var worldWidth = mapColumns * pixelsPerTile;
var worldHeight = mapRows * pixelsPerTile;

var cameraX = 0;
 var cameraY = worldHeight - 300;

var playerWidth = 10;
var playerHeight = 10;

var playerX = 0;
var playerY = worldHeight - 30;

var playerDX = 0;
var playerDY = 0;



function render () {
    context.fillStyle = background;
    context.fillRect(0, 0, worldWidth, worldHeight);

    for (var ii = 0; ii < mapColumns; ++ii) {
        for (var jj = 0; jj < mapRows; ++jj) {
            switch (getMapTile(map,ii,jj)) {
            case 0 :
                continue
            case 1 :
                context.fillStyle = blue;
                break;
            case 2 :
                context.fillStyle = red;
                break;
            default:
                continue;
            }
            context.fillRect(ii * pixelsPerTile - cameraX,
                             jj * pixelsPerTile - cameraY,
                             pixelsPerTile, pixelsPerTile);
        }
    }

    context.fillStyle = navy;
    context.fillRect(playerX - cameraX, playerY - cameraY,
                     playerWidth, playerHeight);
}


function init() {
    console.log("initing");
    canvas = document.getElementById('canvas');
    if (canvas.getContext) {
        context = canvas.getContext('2d');
    }

    render();
}

keys = Array(256);

for (var ii = 0; ii < 256; ++ii) {
    keys[ii] = 0;
}

function playerAct() {
    if (keys[37] == 1) {
        // LEFT
        --playerDX;
    }
    if (keys[38] == 1) {
        // UP
        --playerDY;
    }
    if (keys[39] == 1) {
        // RIGHT
        ++playerDX;
    }
    if (keys[40] == 1) {
        // DOWN
        ++playerDY;
    }
    if (keys[32] == 1){
        // SPACE
        playerDY -= 5;
    }


}


function tick() {
    playerAct();
    playerX += playerDX;
    playerY += playerDY;

    render();
}


function kpress(event) {
// console.log(event)
}

function kup(event) {
// console.log(event)
    if (event.which != 0) {
        keys[event.keyCode] = 0;
    }

}

function kdown(event) {
//    console.log(event);
    if (event.which != 0) {
        keys[event.keyCode] = 1;
    }
}

var interval = window.setInterval(tick, 25);
window.onkeypress = kpress;
window.onkeyup = kup;
window.onkeydown = kdown;
